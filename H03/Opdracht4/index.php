<?php


echo "<h1> Hier begint de while loop </h1>";
echo "<p>bij een while loop weet je nog niet hoevaak je wil herhalen</p>";

$x = 35;

while ($x >= 25) {
    echo "hoppelepee <br>" ;
    $x--;
}


echo "<h1> hier de for loop</h1> ";
echo  "<p> Bij een for loop weet je al hoevaak je wilt herhalen </p> ";

for ($i = 35 ; $i >= 25 ; $i--) {
    echo "hoppelepee <br>";
}

